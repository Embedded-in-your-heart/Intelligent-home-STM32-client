/*
 * BleTask: owns the BlueNRG-MS stack. All ACI / HCI calls are made from this
 * thread to keep the SPI transport serialised. Other tasks will hand work
 * over via queues (added in subsequent milestones).
 */
static void StartBleTask(void *argument)
{
  (void)argument;
  for (;;) {
    MX_BlueNRG_MS_Process();
    NotifyQueue_Pump();            /* Drains sensor pushes and calls Home_*_Update. */
    osDelay(1);
  }
}
